<?php
declare(strict_types=1);

namespace JML\Module\GenericValueObject;

class Mail
{
    const MAIL_PREG_MATCH = '/^(?!(?:(?:\x22?\x5C[\x00-\x7E]\x22?)|(?:\x22?[^\x5C\x22]\x22?)){255,})(?!(?:(?:\x22?\x5C[\x00-\x7E]\x22?)|(?:\x22?[^\x5C\x22]\x22?)){65,}@)(?:(?:[\x21\x23-\x27\x2A\x2B\x2D\x2F-\x39\x3D\x3F\x5E-\x7E]+)|(?:\x22(?:[\x01-\x08\x0B\x0C\x0E-\x1F\x21\x23-\x5B\x5D-\x7F]|(?:\x5C[\x00-\x7F]))*\x22))(?:\.(?:(?:[\x21\x23-\x27\x2A\x2B\x2D\x2F-\x39\x3D\x3F\x5E-\x7E]+)|(?:\x22(?:[\x01-\x08\x0B\x0C\x0E-\x1F\x21\x23-\x5B\x5D-\x7F]|(?:\x5C[\x00-\x7F]))*\x22)))*@(?:(?:(?!.*[^.]{64,})(?:(?:(?:xn--)?[a-z0-9]+(?:-[a-z0-9]+)*\.){1,126}){1,}(?:(?:[a-z][a-z0-9]*)|(?:(?:xn--)[a-z0-9]+))(?:-[a-z0-9]+)*)|(?:\[(?:(?:IPv6:(?:(?:[a-f0-9]{1,4}(?::[a-f0-9]{1,4}){7})|(?:(?!(?:.*[a-f0-9][:\]]){7,})(?:[a-f0-9]{1,4}(?::[a-f0-9]{1,4}){0,5})?::(?:[a-f0-9]{1,4}(?::[a-f0-9]{1,4}){0,5})?)))|(?:(?:IPv6:(?:(?:[a-f0-9]{1,4}(?::[a-f0-9]{1,4}){5}:)|(?:(?!(?:.*[a-f0-9]:){5,})(?:[a-f0-9]{1,4}(?::[a-f0-9]{1,4}){0,3})?::(?:[a-f0-9]{1,4}(?::[a-f0-9]{1,4}){0,3}:)?)))?(?:(?:25[0-5])|(?:2[0-4][0-9])|(?:1[0-9]{2})|(?:[1-9]?[0-9]))(?:\.(?:(?:25[0-5])|(?:2[0-4][0-9])|(?:1[0-9]{2})|(?:[1-9]?[0-9]))){3}))\]))$/iD';
    const MIN_MAIL_LENGTH = 6;

    protected $mail;

    protected function __construct(string $mail)
    {
        $this->mail = $mail;
    }

    public static function fromString(string $mail): self
    {
        self::ensureMailIsValid($mail);
        $mail = self::convertMail($mail);

        return new self($mail);
    }

    protected static function ensureMailIsValid(string $mail): void
    {
        if (preg_match(self::MAIL_PREG_MATCH, $mail) !== 1){
            throw new \InvalidArgumentException('Keine gültige Mailadresse!');
        }

        if (strlen($mail) < self::MIN_MAIL_LENGTH){
            throw new \InvalidArgumentException('Keine gültige Mailadresse!');
        }
    }

    protected static function convertMail(string $mail): string
    {
        return strtolower(trim($mail));
    }

    public function getMail(): string
    {
        return $this->mail;
    }

    public function __toString(): string
    {
        return $this->mail;
    }

}